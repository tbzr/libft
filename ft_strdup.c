#include "libft.h"

char	*ft_strdup(const char *s1)
{
	char	*new;
	int		i;

	i = 0;
	if (s1 == NULL)
		return (NULL);
	if (NULL == (new = ft_memalloc(sizeof(char *) * (ft_strlen(s1) + 1))))
		return (NULL);
	while (s1[i])
	{
		new[i] = s1[i];
		i++;
	}
	new[i] = '\0';
	return (new);
}
