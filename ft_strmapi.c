#include "libft.h"

char	*ft_strmapi(char const *s, char (*f)(unsigned int i, char))
{
	unsigned int	i;
	char			*new;

	i = 0;
	new = ft_memalloc(sizeof(char *) * (ft_strlen(s)));
	while (s[i])
	{
		new[i] = (*f)(i, s[i]);
		i++;
	}
	new[i] = '\0';
	return (new);
}
